export interface IDimensionsProps {
  /**
   * Define minimum width in px
   */
  minWidth?: number
  /**
   * Define maximum width in px
   */
  maxWidth?: number
  /**
   * Define element width in px
   */
  width?: number
  /**
   * Define minimum height in px
   */
  minHeight?: number
  /**
   * Define maximum height in px
   */
  maxHeight?: number
  /**
   * Define element height in px
   */
  height?: number
  /**
   * Set element padding
   */
  padding?: string
  /**
   * Set element margin
   */
  margin?: string
  /**
   * Should element fill all available width
   */
  isFullWidth?: boolean
  /**
   * Define element border radius
   */
  borderRadius?: string
  /**
   * Define element border
   */
  border?: string
}
